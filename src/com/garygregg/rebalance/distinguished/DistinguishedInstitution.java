package com.garygregg.rebalance.distinguished;

public enum DistinguishedInstitution {

    /*
     * In the values that follow, "first" applies in a temporal sense. The
     * temporal consideration of the annuity providers is in reverse
     * alphabetical order to their names.
     */

    // The large fund complex
    COMPLEX,

    // The 401k provider of a contract employer
    CONTRACT_EMPLOYER,

    // The default (unused)
    DEFAULT,

    // The 401k provider of a full-time employer
    FULL_TIME_EMPLOYER,

    // The provider of the health savings account
    HEALTH_SAVINGS,

    // My own 'institution'
    SELF
}
